package model;

/**
 *
 * @author Willian
 */
public class Ponto {
    private float x;
    private float y;
    private int classe;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getClasse() {
        return classe;
    }

    public void setClasse(int classe) {
        this.classe = classe;
    }

}//fim class Ponto
